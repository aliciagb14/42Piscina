int		ft_sqrt(int nb)
{
	int i;
	int res;

	i = 0;
	res = 0;
	while (i < 46340)
	{
		if (nb < 0)
		{
			return (0);
		}
		if (i < nb)
		{
			res = i * i;
			if (res == nb)
			{
				return (i);
			}
			else if (res > nb)
				return (0);
			i++;
		}
	}
	return (0);
}
