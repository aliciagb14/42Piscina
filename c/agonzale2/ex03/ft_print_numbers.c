#include <unistd.h>

void	ft_print_numbers (void);

void	ft_print_numbers(void)
{
	int		n;
	char	c;

	n = 0;
	c = '0';
	while (n < 10)
	{
		write(1, &c, 1);
		n++;
		c++;
	}
}
